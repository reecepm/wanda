import { useAutoGeneratedFiles } from '@/features/git/hooks/use-auto-generated'
import { useReviewComments } from '@/features/git/hooks/use-review'
import { useGitDiffStore } from '@/features/git/store/git-diff-store'
import { getCommentsForFile, useReviewStore } from '@/features/git/store/review-store'
import { useViewedFilesStore } from '@/features/git/store/viewed-files-store'
import { type FilePatchInfo, splitAndParsePatch } from '@/features/git/utils/diff-utils'
import type { DiffMode } from '@/features/git/utils/git-status'
import { orpcUtils } from '@/shared/orpc'
